import mongoose, { Schema } from 'mongoose'
import { IAddressBook } from './addressBook.interface'

const addressBookSchema = new Schema<IAddressBook>(
  {
    fulName: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      required: true,
    },
    province: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    area: {
      type: String,
      required: true,
    },
    address: {
      type: String,
      required: true,
    },
    defaultDeliveryAddress: {
      type: Boolean,
      default: false,
    },
    userId: {
      type: Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
  },
  {
    timestamps: true,
    toJSON: {
      virtuals: true,
    },
  },
)

export const AddressBook = mongoose.model('addressbook', addressBookSchema)
